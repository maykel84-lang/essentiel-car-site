/* ── i18n Engine ── */
let currentLang = localStorage.getItem('ec_lang') || 'fr';

function t(key) {
  return TRANSLATIONS[currentLang][key] || TRANSLATIONS['fr'][key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.getAttribute('data-i18n-title'));
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
  });

  const htmlEl = document.documentElement;
  htmlEl.lang = currentLang;

  const titleEl = document.querySelector('title[data-i18n]');
  if (titleEl) document.title = currentLang === 'en' ? 'ESSENTIEL CAR — Premium Auto Equipment' : 'ESSENTIEL CAR — L\'équipement auto premium';
}

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('ec_lang', lang);

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  applyTranslations();

  if (typeof renderProducts === 'function') renderProducts();
  if (typeof renderReviews === 'function') renderReviews();
  if (typeof renderCategories === 'function') renderCategories();
  if (typeof renderProblems === 'function') renderProblems();

  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });
  setLang(currentLang);
});
